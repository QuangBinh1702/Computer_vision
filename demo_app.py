                chosen_idx = st.number_input(
                    f"Chọn ảnh (1–{len(imgs)})", min_value=1,
                    max_value=len(imgs), value=1, step=1,
                )
                image = Image.open(imgs[chosen_idx - 1])
                selected_label = chosen_flower

