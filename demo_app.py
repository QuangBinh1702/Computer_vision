# ── Test set images for browsing ─────────────────────────────────────────
TEST_DIR = ROOT / "flower_data" / "flower_data" / "test"

@st.cache_data
def get_test_images() -> dict[str, list[Path]]:
    """Return {flower_name: [image_paths]} from test set."""
    result: dict[str, list[Path]] = {}
    if not TEST_DIR.exists():
        return result
    for folder in sorted(TEST_DIR.iterdir()):
        if folder.is_dir() and folder.name in CAT_TO_NAME:
            name = CAT_TO_NAME[folder.name]
            imgs = sorted(folder.glob("*.jpg")) + sorted(folder.glob("*.png"))
            if imgs:
                result[name] = imgs
    return result

# ── Tab: Predict ────────────────────────────────────────────────────────
with tab_predict:
    col_upload, col_result = st.columns([1, 1], gap="large")

    with col_upload:
        input_mode = st.radio(
            "Nguồn ảnh", ["📂 Chọn từ Test Set", "📤 Upload ảnh"],
            horizontal=True,
        )

        image = None
        selected_label = None

        if input_mode == "📂 Chọn từ Test Set":
            test_images = get_test_images()
            if test_images:
                flower_names = list(test_images.keys())
                chosen_flower = st.selectbox("Chọn loại hoa", flower_names)
                imgs = test_images[chosen_flower]
                # Show thumbnail grid to pick from
